void AES::subbytes(uint8_t *a) {
  for (int i = 0; i < 16; i++)
    a[i] = sbox[a[i]];
}

void AES::subbytes_inv(uint8_t *a) {
  for (int i = 0; i < 16; i++)
    a[i] = sbox_inv[a[i]];
}

#define SWAP2(a, b)                                                            \
  {                                                                            \
    auto t = a;                                                                \
    a = b;                                                                     \
    b = t;                                                                     \
  }
#define SWAP4(a, b, c, d)                                                      \
  {                                                                            \
    auto t = a;                                                                \
    a = b;                                                                     \
    b = c;                                                                     \
    c = d;                                                                     \
    d = t;                                                                     \
  }

void AES::shiftrows(uint8_t *a) {
  SWAP4(a[1], a[5], a[9], a[13]);
  SWAP2(a[2], a[10]);
  SWAP2(a[6], a[14]);
  SWAP4(a[15], a[11], a[7], a[3]);
}

void AES::shiftrows_inv(uint8_t *a) {
  SWAP4(a[13], a[9], a[5], a[1]);
  SWAP2(a[2], a[10]);
  SWAP2(a[6], a[14]);
  SWAP4(a[3], a[7], a[11], a[15]);
}

#define M1(x) x
#define M2(x) m2[x]
#define M3(x) m3[x]
#define M9(x) m9[x]
#define Mb(x) mb[x]
#define Me(x) me[x]
#define Md(x) md[x]

void AES::mixcolumns(uint8_t *a) {
  for (int i = 0; i < 4; i++) {
    uint8_t t[4];
    t[0] =
        M2(a[4 * i]) ^ M3(a[4 * i + 1]) ^ M1(a[4 * i + 2]) ^ M1(a[4 * i + 3]);
    t[1] =
        M1(a[4 * i]) ^ M2(a[4 * i + 1]) ^ M3(a[4 * i + 2]) ^ M1(a[4 * i + 3]);
    t[2] =
        M1(a[4 * i]) ^ M1(a[4 * i + 1]) ^ M2(a[4 * i + 2]) ^ M3(a[4 * i + 3]);
    t[3] =
        M3(a[4 * i]) ^ M1(a[4 * i + 1]) ^ M1(a[4 * i + 2]) ^ M2(a[4 * i + 3]);
    std::memcpy(a + 4 * i, t, 4);
  }
}

void AES::mixcolumns_inv(uint8_t *a) {
  for (int i = 0; i < 4; i++) {
    uint8_t t[4];
    t[0] =
        Me(a[4 * i]) ^ Mb(a[4 * i + 1]) ^ Md(a[4 * i + 2]) ^ M9(a[4 * i + 3]);
    t[1] =
        M9(a[4 * i]) ^ Me(a[4 * i + 1]) ^ Mb(a[4 * i + 2]) ^ Md(a[4 * i + 3]);
    t[2] =
        Md(a[4 * i]) ^ M9(a[4 * i + 1]) ^ Me(a[4 * i + 2]) ^ Mb(a[4 * i + 3]);
    t[3] =
        Mb(a[4 * i]) ^ Md(a[4 * i + 1]) ^ M9(a[4 * i + 2]) ^ Me(a[4 * i + 3]);
    std::memcpy(a + 4 * i, t, 4);
  }
}

void AES::addroundkey(uint32_t *buf, int r) {
  uint32_t *key = key_ + 4 * r;
  buf[0] ^= key[0];
  buf[1] ^= key[1];
  buf[2] ^= key[2];
  buf[3] ^= key[3];
}

AES::AES(const uint8_t *key, int size) {
  switch (size) {
  case 16:
    Nk_ = 4;
    Nr_ = 11;
    std::memcpy(key_, key, 16);
    break;
  case 24:
    Nk_ = 6;
    Nr_ = 13;
    std::memcpy(key_, key, 24);
    break;
  case 32:
    Nk_ = 8;
    Nr_ = 15;
    std::memcpy(key_, key, 32);
    break;
  default:
    throw std::exception();
  }

  for (int i = Nk_; i < 4 * Nr_; i++) {
    if (!(i % Nk_)) {
      uint8_t *key = (uint8_t *)(key_ + i);
      key[0] = sbox[key[-3]] ^ rcon[i / Nk_ - 1];
      key[1] = sbox[key[-2]];
      key[2] = sbox[key[-1]];
      key[3] = sbox[key[-4]];
      key_[i] ^= key_[i - Nk_];
    } else if (Nk_ > 6 && !(i % 4)) {
      uint8_t *key = (uint8_t *)(key_ + i);
      key[0] = sbox[key[-4]];
      key[1] = sbox[key[-3]];
      key[2] = sbox[key[-2]];
      key[3] = sbox[key[-1]];
      key_[i] ^= key_[i - Nk_];
    } else {
      key_[i] = key_[i - 1] ^ key_[i - Nk_];
    }
  }
}

void AES::encrypt(uint32_t *buf) {
  addroundkey(buf, 0);
  for (int i = 1; i <= Nr_ - 2; i++) {
    subbytes((uint8_t *)buf);
    shiftrows((uint8_t *)buf);
    mixcolumns((uint8_t *)buf);
    addroundkey(buf, i);
  }
  subbytes((uint8_t *)buf);
  shiftrows((uint8_t *)buf);
  addroundkey(buf, Nr_ - 1);
}

void AES::decrypt(uint32_t *buf) {
  addroundkey(buf, Nr_ - 1);
  for (int i = Nr_ - 2; i >= 1; i--) {
    shiftrows_inv((uint8_t *)buf);
    subbytes_inv((uint8_t *)buf);
    addroundkey(buf, i);
    mixcolumns_inv((uint8_t *)buf);
  }
  shiftrows_inv((uint8_t *)buf);
  subbytes_inv((uint8_t *)buf);
  addroundkey(buf, 0);
}

AESCTR::AESCTR(const uint8_t *key, int size, const uint8_t *iv, uint32_t ctr)
    : AES(key, size), ctr_(ctr) {
  std::memcpy(iv_, iv, 12);
}

void AESCTR::ek(uint32_t *buf) {
  std::memcpy(buf, iv_, 12);
  buf[3] = htobe32(ctr_++);
  encrypt(buf);
}

uint8_t *AESCTR::ctr(const uint8_t *plain, int size) {
  uint8_t *cipher = new uint8_t[size];
  uint32_t buf[4];
  for (int i = 0, cur = 16; i < size; i++) {
    if (cur >= 16) {
      ek(buf);
      cur = 0;
    }
    cipher[i] = plain[i] ^ ((uint8_t *)buf)[cur++];
  }
  return cipher;
}

AESGCM::AESGCM(const uint8_t *key, int size, const uint8_t *iv)
    : AESCTR(key, size, iv) {
  h_[0] = h_[1] = 0;
  encrypt((uint32_t *)h_);
  h_[0] = be64toh(h_[0]);
  h_[1] = be64toh(h_[1]);
  tag_[0] = tag_[1] = 0;
  ek((uint32_t *)ek1_);
};

void AESGCM::gmul() {
  uint64_t res[2] = {0, 0};
  for (int i = 0; i < 2; i++) {
    for (int j = 63; j >= 0; j--) {
      if (h_[i] & ((uint64_t)1 << j)) {
        res[0] ^= tag_[0];
        res[1] ^= tag_[1];
      }
      int flag0 = tag_[0] & 1;
      int flag1 = tag_[1] & 1;
      tag_[1] >>= 1;
      tag_[0] >>= 1;
      if (flag0)
        tag_[1] |= 0x8000000000000000;
      if (flag1)
        tag_[0] ^= 0xe100000000000000;
    }
  }
  tag_[0] = res[0];
  tag_[1] = res[1];
}

void AESGCM::update_tag(const uint8_t *data, int size) {
  uint64_t buf[2];
  while (size > 16) {
    std::memcpy(buf, data, 16);
    tag_[0] ^= be64toh(buf[0]);
    tag_[1] ^= be64toh(buf[1]);
    gmul();
    data += 16;
    size -= 16;
  }
  std::memset(buf, 0, 16);
  std::memcpy(buf, data, size);
  tag_[0] ^= be64toh(buf[0]);
  tag_[1] ^= be64toh(buf[1]);
  gmul();
}

void AESGCM::gcm(const uint8_t *add, int add_size, const uint8_t *plain,
                 int plain_size, uint8_t *&cipher, uint8_t *&tag) {
  cipher = ctr(plain, plain_size);
  tag = new uint8_t[16];

  update_tag(add, add_size);
  update_tag(cipher, plain_size);

  tag_[0] ^= (uint64_t)add_size << 3;
  tag_[1] ^= (uint64_t)plain_size << 3;
  gmul();

  tag_[0] = htobe64(tag_[0]) ^ ek1_[0];
  tag_[1] = htobe64(tag_[1]) ^ ek1_[1];
  std::memcpy(tag, tag_, 16);
}
